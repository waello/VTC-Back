package com.navettevatry.rem4u.common.resources.dto.allocab.updated;

public class Options {
    private boolean clim;
    private boolean water;
    private boolean wifi;
    private boolean elec;
    private boolean music;

    public boolean getClim() { return clim; }
    public void setClim(boolean value) { this.clim = value; }

    public boolean getWater() { return water; }
    public void setWater(boolean value) { this.water = value; }

    public boolean getWifi() { return wifi; }
    public void setWifi(boolean value) { this.wifi = value; }

    public boolean getElec() { return elec; }
    public void setElec(boolean value) { this.elec = value; }

    public boolean getMusic() { return music; }
    public void setMusic(boolean value) { this.music = value; }
}
