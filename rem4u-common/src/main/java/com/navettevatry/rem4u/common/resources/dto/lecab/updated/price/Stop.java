public class Stop {
    private Address address;
    private Object arrivalDetails;
    private Object contact;
    private Object id;
    private Object notes;

    public Address getAddress() {
        return address;
    }

    public void setAddress(Address value) {
        this.address = value;
    }

    public Object getArrivalDetails() {
        return arrivalDetails;
    }

    public void setArrivalDetails(Object value) {
        this.arrivalDetails = value;
    }

    public Object getContact() {
        return contact;
    }

    public void setContact(Object value) {
        this.contact = value;
    }

    public Object getID() {
        return id;
    }

    public void setID(Object value) {
        this.id = value;
    }

    public Object getNotes() {
        return notes;
    }

    public void setNotes(Object value) {
        this.notes = value;
    }
}
