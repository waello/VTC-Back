package com.navettevatry.rem4u.common.resources.enumeration;

public class Element {
    private Distance distance;
    private Distance duration;
    private String status;

    public Distance getDistance() {
        return distance;
    }

    public void setDistance(Distance value) {
        this.distance = value;
    }

    public Distance getDuration() {
        return duration;
    }

    public void setDuration(Distance value) {
        this.duration = value;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String value) {
        this.status = value;
    }
}
